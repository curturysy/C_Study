#include <stdio.h>


int main()
{

    //单精度浮点数转换为整数
    float a = 3.141592;
    printf("%d\n",(int) a);

    //整数转化为单精度浮点数
    int b = 3;
    printf("%f\n",(float) b);

    //字符转化为整数
    char c = 'a';
    printf("%d\n",c);

    //整数转化为字符
    int d = 1;
    printf("%c\n",d);




    return 0;
}